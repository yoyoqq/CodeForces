void solve(){
    int n; cin >> n;
    vector<int> a(n); for(int& x : a) cin >> x;    
    // 
    // cannot be 1 
    for (int& x : a) if (x == 1) x++;
    // if divisible then add 1 -> even/odd will always work 
    for (int i=1; i<n; i++){
        if (a[i] % a[i-1] == 0){
            a[i]++;
        }
    }
    for(int& x : a) cout << x << " "; cout << endl;
}

